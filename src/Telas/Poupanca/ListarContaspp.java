 private void table() {
        Poupanca contacc = new Poupanca();
        try {
            List<Poupanca> l = contacc.listar();
            for (Poupanca u : l) {
                Object[] o = {u.getAgencia(), u.getNumeroConta(), u.getUsuario()};
                ((DefaultTableModel) jTable1.getModel()).addRow(o);
            }
        } catch (IOException ex) {

        }
    }
