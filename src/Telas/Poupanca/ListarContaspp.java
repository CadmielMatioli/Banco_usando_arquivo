    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        if (evt.getClickCount() == 2) {
            try {
                EditarContapp e = new EditarContapp();

                JTable table = (JTable) evt.getSource();
                int row = table.getSelectedRow();
                String o = (String) table.getValueAt(row, 0);
                Poupanca u = new Poupanca();
                u.setNumeroConta(o);
                try {
                    u.buscar();
                } catch (IOException ex) {
                    Logger.getLogger(ListarContaspp.class.getName()).log(Level.SEVERE, null, evt);
                } catch (Exception ex) {
                    Logger.getLogger(ListarContaspp.class.getName()).log(Level.SEVERE, null, ex);
                }
                e.setU(u);
                e.setVisible(true);
                dispose();
            } catch (IOException ex) {
                Logger.getLogger(ListarContaspp.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

